class Baloon{
	public static void main(String[] args){
		System.out.println("RUN MAIN IN BALOON");
		System.out.println();
		
		String shape = "Circular";
		float size = 30.5f;
		String message = "HAPPY BIRTHDAY";
		String color = "RED";
		short noOfBaloons = 500;
		
		System.out.println("1. SHAPE : "+shape);
		System.out.println("2. SIZE OF THE BALOON : "+size);
		System.out.println("3. MESSAGE : "+message);
		System.out.println("4. COLOR OF BALOON : "+color);
		System.out.println("5. NUMBER OF BALOONS : "+noOfBaloons);
	}
}