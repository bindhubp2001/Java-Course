class IfElseLadder{
	public static void main(String[] args)
	{
		char grade='S';
		if(grade=='S')
		{
			System.out.println("OUTSTANDING");
		}
		else if(grade=='A')
		{
			System.out.println("VERY GOOD");
		}
		else if(grade=='B')
		{
			System.out.println("GOOD");
		}
		else{
			System.out.println("POOR");
		}
	}
}