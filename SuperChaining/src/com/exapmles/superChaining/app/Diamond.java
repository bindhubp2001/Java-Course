package com.exapmles.superChaining.app;

public class Diamond {
	public boolean isElectricResistivity;
	public double hardness;
	public String color;
	public double weight;
	
	public Diamond(boolean isElectricResistivity,double hardness,String color,double weight) {
		this.color=color;
		this.hardness=hardness;
		this.isElectricResistivity=isElectricResistivity;
		this.weight=weight;
	}
}
